    private void write(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_write
        char decimalSeparator;
        char separator;
        
        if(Locale.getDefault().getLanguage().equals("es")) {
            decimalSeparator = ',';
            separator = '.';
        } else {
            decimalSeparator = '.';
            separator = ',';
        }
        
        if(evt.getKeyChar() == decimalSeparator) {
            if(_textField.getText().contains(String.valueOf(decimalSeparator))) {
                evt.consume();
            } else if(_textField.getText().isEmpty()){
                evt.consume();
            }
        } else if(evt.getKeyChar() == '\b') {
            if(!_textField.getText().isEmpty()) {
                String aux = _textField.getText();
                aux = aux.replace(String.valueOf(separator), "");
                if(!aux.contains(String.valueOf(decimalSeparator))){
                    long quantity = Long.parseLong(aux);
                    DecimalFormat format = new DecimalFormat("#,###");
                    _textField.setText(format.format(quantity));
                }
            }
        } else if(evt.getKeyChar() < 48 || evt.getKeyChar() > 57) {
            evt.consume();
        } else {
            if(_textField.getText().contains(String.valueOf(decimalSeparator))) {
                if(_textField.getText().indexOf(decimalSeparator) < _textField.getText().length()-2) {
                    evt.consume();
                } else if(_textField.getText().charAt(_textField.getText().length()-1) == '0'
                       && evt.getKeyChar() == '0') {
                    evt.consume();
                }
            } else {
                String aux = _textField.getText();
                aux = aux.replace(String.valueOf(separator), "");
                aux = aux.concat(String.valueOf(evt.getKeyChar()));
                long quantity = Long.parseLong(aux);
                DecimalFormat format = new DecimalFormat("#,###");
                _textField.setText(format.format(quantity));
                evt.consume();
            }
        }
    }//GEN-LAST:event_write

