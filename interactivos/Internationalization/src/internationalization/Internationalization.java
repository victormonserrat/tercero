    private void spanish(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_spanish
        if(!"es".equals(Locale.getDefault().getLanguage())) {
            Locale.setDefault(new Locale("es", "ES"));
            _textField.setHorizontalAlignment(JTextField.RIGHT);
            if(!_textField.getText().isEmpty()) {
                String aux = _textField.getText();
                aux = aux.replace(",", "");
                double quantity = Double.parseDouble(aux);
                DecimalFormat format;
                if(aux.contains(".")) {
                    if(aux.charAt(aux.length()-1) == '0' &&
                       aux.charAt(aux.length()-2) == '.') {
                        format = new DecimalFormat("#,###");
                    } else if(aux.charAt(aux.length()-2) == '.') {
                        format = new DecimalFormat("#,###.0");
                    }else if(aux.charAt(aux.length()-1) == '.') {
                        format = new DecimalFormat("#,###");
                    }else if(aux.charAt(aux.length()-1) == '0') {
                        format = new DecimalFormat("#,###.0");
                    } else {
                        format = new DecimalFormat("#,###.00");
                    }
                } else {
                    format = new DecimalFormat("#,###");
                }
                _textField.setText(format.format(quantity));
                if(_textField.getText().charAt(0) == ','){
                    _textField.setText("0" + _textField.getText());
                }
            }
            translate();
        }
    }//GEN-LAST:event_spanish

